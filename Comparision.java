public class Comparision {
    public static void main(String[] args) {
        int a = 15, b = 20;
        System.out.println(a+"<"+b+" is "+(a<b));
        System.out.println(a+"<="+b+" is "+(a<=b));
        System.out.println(a+">"+b+" is "+(a>b));
        System.out.println(a+">="+b+" is "+(a>=b));
        System.out.println(a+"=="+b+" is "+(a==b));
        System.out.println(a+"!="+b+" is "+(a!=b));
    }
    
}
